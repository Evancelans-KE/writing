SELECT a.ORDER_ID, b.COMPANY_NAME, b.CONTACT_NAME, TO_CHAR(a.SHIPPED_DATE,'YYYY-MM-DD'),
CASE WHEN
'2009-01-01' MINUS TO_CHAR(a.SHIPPED_DATE,'YYYY-MM-DD') THEN COUNT(YEAR('2009-01-01' MINUS TO_CHAR(a.SHIPPED_DATE,'YYYY-MM-DD')))
FROM ORDERS a 
LEFT OUTER JOIN CUSTOMERS b 
ON a.ORDER_ID = b.ORDER_ID
WHERE TO_CHAR(a.SHIPPED_DATE,'YYYY-MM-DD') >= '01-01-1994'
ORDER BY a.ORDER_ID ASC


-------------------------------------------------------------------------------

SELECT a.ORDER_ID, b.PRODUCT_NAME,c.COMPANY_NAME,a.ORDER_DATE,
CASE WHEN (DAY(TO_CHAR(a.SHIPPED_DATE,'YYYY-MM-DD'))) + 10 THEN 'NEWSHIPPED_DATE',
(a.QUANTITY*a.UNIT_PRICE)ORDER_COST
FROM ORDERS a 
LEFT OUTER JOIN PRODUCTS b
on a.PRODUCT_ID = b.PRODUCT_ID
LEFT OUTER JOIN CUSTOMERS
ON a.CUSTOMER_ID = c.CUSTOMER_ID
WHERE a.ORDER_DATE >= '01-01-1992' AND a.ORDER_DATE <= '30-03-1992'
ORDER BY a.ORDER_ID ASC


