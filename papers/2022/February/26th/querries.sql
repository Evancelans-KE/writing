SELECT a.SUPPLIER_ID,a.SUPPLIER_NAME,b.PRODUCT_NAME,
sum(b.REORDER_LEVEL),sum(b.UNITS_INSTOCK) FROM PRODUCTS a
LEFT OUTER JOIN SUPPLIERS b 
ON a.PRODUCT_ID = b.PRODUCT_ID
WHERE sum(b.REORDER_LEVEL) = sum(b.UNITS_INSTOCK) 
ORDER BY b.SUPPLIER_ID ASC

